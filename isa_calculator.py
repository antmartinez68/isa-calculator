"""Módulo calculadora muy básico"""


def add(a, b):
    """Sumar dos valores"""
    return a + b


def sub(a, b):
    """Restar a un valor otro"""
    return a - b


def mul(a, b):
    """Multiplicar dos valores"""
    return a * b


def div(a, b):
    """Dividir un valor entre otro"""
    return a / b
